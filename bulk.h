double fn_R_A(double t){
  double Rf = 1.0*RS/150e9;
  double tf = 2.0e7*(365.25*86400)/5022086.559303;
  double m  = (Rf-0.009301)/(tf-0.0);
  double rr = m*t + 0.009301;
  return rr;
 
}

